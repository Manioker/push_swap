int	check_for_char(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z') && (str[i] >= 'a'
				&& str[i] <= 'z'))
		{
			return (-1);
		}
		i++;
	}
	return (1);
}
