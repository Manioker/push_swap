#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	a = 0;
	b = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			b = -b;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		a = (a * 10) + (str[i] - '0');
		i++;
	}
	return (a * b);
}

